package io.naftiko.exposes.spec;

import java.util.List;
import java.util.concurrent.CopyOnWriteArrayList;
import com.fasterxml.jackson.annotation.JsonInclude;
import io.naftiko.spec.OperationSpec;

/**
 * API Operation Specification Element
 */
public class ApiOperationSpec extends OperationSpec {

    @JsonInclude(JsonInclude.Include.NON_EMPTY)
    private final List<ApiStepSpec> steps;

    public ApiOperationSpec() {
        this(null, null, null, null, null, null);
    }

    public ApiOperationSpec(ApiResourceSpec parentResource, String method, String name, String label) {
        this(parentResource, method, name, label, null, null);
    }

    public ApiOperationSpec(ApiResourceSpec parentResource, String method, String name, String label, String description, String outputRawFormat) {
        super(parentResource, method, name, label, description, outputRawFormat);
        this.steps = new CopyOnWriteArrayList<>();
    }

    public List<ApiStepSpec> getSteps() {
        return steps;
    }

}
